#!/usr/bin/python3

# Define the variable word
word = "Holberton"

# Copy the first 3 letters of word to word_first_3
word_first_3 = word[:3]

# Cut the last 2 letters of word to word_last_2
word_last_2 = word[-2:]

# Paste the middle letters of word to middle_word
middle_word = word[1:-1]

# Print the results
print("First 3 letters:", word_first_3)
print("Last 2 letters:", word_last_2)
print("Middle word:", middle_word)
